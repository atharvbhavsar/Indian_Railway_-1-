export function LanguageProvider({ children }) {
  const { currentUser, updateUserLanguage } = useAuth();
  const [language, setLanguageState] = useState("en");

  // Load language when component mounts or user session loads
  useEffect(() => {
    const localLang = localStorage.getItem("rses_preferred_language");
    if (localLang) {
      setLanguageState(localLang);
    } else if (currentUser && currentUser.preferredLanguage) {
      setLanguageState(currentUser.preferredLanguage);
    }
  }, [currentUser]);

  const changeLanguage = async (newLang) => {
    if (!["en", "hi", "mr"].includes(newLang)) return;

    setLanguageState(newLang);
    localStorage.setItem("rses_preferred_language", newLang);

    // Sync with Auth Context
    updateUserLanguage(newLang);

    // Sync with DB if user is logged in
    if (currentUser && currentUser.userId) {
      try {
        await userService.updatePreferredLanguage(currentUser.userId, newLang);
      } catch (err) {
        console.error("Failed to sync language preference with DB", err);
      }
    }
  };

  const t = (text) => {
    if (!text) return "";
    const key = String(text).trim();
    if (translations[language] && translations[language][key]) {
      return translations[language][key];
    }
    return text;
  };

  return (
    <LanguageContext.Provider value={{ language, changeLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error("useLanguage must be used within a LanguageProvider");
  }
  return context;
}
